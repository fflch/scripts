function import_csv_to_nodes($file_path, $field_mapping) {
  if (($handle = fopen($file_path, "r")) !== FALSE) {

    $line_1 = fgetcsv($handle, 1000, ",");
    if ($line_1 === FALSE) {
      \Drupal::logger('import_csv')->error('Erro ao ler a primeira linha do CSV.');
      fclose($handle);
      return;
    }

    $ano = trim($line_1[0]);
    
    $header_line_2 = fgetcsv($handle, 1000, ",");
    $header_line_3 = fgetcsv($handle, 1000, ",");

    // Verifique se os cabeçalhos foram lidos corretamente
    if ($header_line_2 === FALSE || $header_line_3 === FALSE) {
      \Drupal::logger('import_csv')->error('Erro ao ler cabeçalhos do CSV.');
      fclose($handle);
      return;
    }

    // Combine os cabeçalhos, preenchendo campos vazios
    $header = [];
    foreach ($header_line_2 as $index => $header_name) {
      $header[$index] = !empty(trim($header_name)) ? trim($header_name) : trim($header_line_3[$index]);
    }

    // Mapeie os índices das colunas com base nos cabeçalhos
    $header_index_map = [];
    foreach ($header as $index => $header_name) {
      if (isset($field_mapping[$header_name])) {
        $header_index_map[$field_mapping[$header_name]] = $index;
      }
    }

    $base_directory = '/home/maria_clara/bibliotecas/Arquivos/';
    $destination_directory = 'public://imported_files/';
    \Drupal::service('file_system')->prepareDirectory($destination_directory, \Drupal\Core\File\FileSystemInterface::CREATE_DIRECTORY);

